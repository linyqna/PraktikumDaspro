package Array.Jobsheet9;

public class Number09 {
    public static void main(String[] args) {
        int num[] = {5, 12, 7, 20};

        System.out.println(num[0]);
        System.out.println(num[1]);
        System.out.println(num[2]);
        System.out.println(num[3]);

        for (int i = 0; i <= 4; i++) {
            System.out.println(num[i]);
        }
    }
}
